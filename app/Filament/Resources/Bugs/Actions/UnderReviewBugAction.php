<?php

namespace App\Filament\Resources\Bugs\Actions;

use App\Enums\BugStatus;
use App\Models\Bug;
use Filament\Actions\Action;
use Filament\Notifications\Notification;

class UnderReviewBugAction extends Action
{
    public static function make(?string $name = null): static
    {
        return parent::make($name); // TODO: Change the autogenerated stub
    }

    protected function setUp(): void
    {
        parent::setUp(); // TODO: Change the autogenerated stub

        $this
            ->label('Mark as Under-Review')
            ->color('warning')
            ->icon('')
            ->requiresConfirmation()
            ->modalHeading('Mark Bug as Under-Review')
            ->modalDescription('Are you sure you want to mark this bug as under-review? This will move it to pending confirmation.')
            ->modalSubmitActionLabel('Yes, Mark Under-Review')
            ->visible(fn (?Bug $bug) => $bug && ! $bug->is_paid && in_array($bug->status, [BugStatus::SUBMITTED, BugStatus::CLOSED, BugStatus::REJECTED, BugStatus::FIXED, BugStatus::DUPLICATE]))
            ->action(function (Bug $bug) {
                $bug->update([
                    'status' => BugStatus::UNDER_REVIEW,
                    'duplicate_of_id' => null,
                    'base_amount' => 0,
                    'final_amount' => 0,
                ]);
                Notification::make()
                    ->title('Bug Marked as Under-Review')
                    ->body("Bug #{$bug->bug_no} has been marked as under-review.")
                    ->success()
                    ->send();
            });
    }
}
